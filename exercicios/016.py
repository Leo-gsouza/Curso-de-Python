""" Obitendo a parte inteira usando Import math » trunc """

import math

numero = float(input('Digite um número: '))
numero = math.trunc(numero)

print(f'A porção inteira do número é {numero}')



""" Obtendo a parte inteira usando INT """

numero = float(input('Digite um número: '))

print (f'A parte inteira do número é {int(numero)}')